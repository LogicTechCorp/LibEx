package logictechcorp.libraryex.utility;

import java.io.File;

public class FileHelper
{
    public static String getFileExtension(File file)
    {
        int dotIndex = file.getName().lastIndexOf('.');
        return dotIndex == -1 ? "" : file.getName().substring(dotIndex + 1);
    }
}
